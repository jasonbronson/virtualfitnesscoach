                        <h3 style="margin-top:20px;margin-bottom:20px;">The Impact of Rising Obesity</h3>
                        <p>Obesity and being overweight is a severe problem facing a vast majority of adults, families and companies throughout the country. Improving the health, wellness and weight of employees and their families can significantly cut company costs on insurance and health, as well as lost work days due to sickness and/or injury for employees. Moreover, employee well being and quality of life can greatly be improved through an effective wellness program both short term and long term, promoting a healthier lifestyle for employees and their families.</p>
                        <p style="margin-left:40px;margin-top:20px;">In a research study conducted by the World Cancer Research Fund and the American Institute for Cancer Research, researchers found that healthier living would prevent 43% of colon cancer cases and 42% of breast cancer in cases in Britain, and 45% of bowel cancer and 38% of breast cancer cases in the United States.<sup>1</sup></p>
                        <p style="margin-left:40px;margin-top:20px;">The National Center for Health Statistics<sup>2</sup> reports that the prevalence of obesity in adult Americans is 32.2%, and the American Academy of Child &amp; Adolescent Psychiatry3 reports between 16-33% of children and adolescents are obese.</p>
                        <h3 style="margin-top:20px;margin-bottom:20px;">Costs Incurred by Obese Workers</h3>
                        <p>Here are reported cost increases and data analysis resulting from obese adults:</p>
                          <ul style="margin-left:40px;margin-top:20px;">
                            <li style="margin-top:20px;">The Kaiser Family Foundation reports: Nationwide, employer-sponsored health coverage premiums for family coverage have increased by 97% since 2000, from $6,438 to $12,680 in 2008.4 Many have experienced 16% increases during the last 3 years much due to the rising epidemic of obesity and overweight adults.</li>
                            <li style="margin-top:20px;">A study in the journal Health Affairs<sup>5</sup>, noted that per person health care spending for obese adults is 56 percent higher than for normal-weight adults. Over 15 years, the additional costs incurred by obese adults with private health insurance versus normal-weight adults increased from $272 to $1,244 per person per year. The International Journal of Obesity reports, weight gains of 20 pounds are associated with medical care cost increases of >$500 over the last three years.<sup>6</sup></li>
                            <li style="margin-top:20px;">Obese workers lose about 13 times more days per year of work from injury or illness.<sup>7</sup> (In an organization of 10,000 employees, with 32% obesity, that equates to 334,880 hours or an estimated 161 full time employees. With an average national salary of $38,500, the total cost of lost days can be as much as: $6,198,500 per year.)</li>
                            <li style="margin-top:20px;">Obese workers are 2 times as likely to file for workers’ compensation<sup>8</sup>, which the Workers Compensation Research Institute (WCRI)<sup>9</sup> reports a state average claim for physicians to be $2,973 and hospitals to be $5,111.</li>
                            <li style="margin-top:20px;">A Medical News Today article states that through a Duke University Medical Analysis, the average medical claimscosts per 100 employees were $51,019 for the obese and $7,503 for the non-obese.<sup>10</sup></li>
                        </ul>

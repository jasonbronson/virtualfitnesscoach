                             The statistic that 330 people turn 60 every hour is certainly reflected on the memberships of gyms and health clubs across the country.  A fun pastime at the gym is trying to match up the elderly guys in the baggy shirts and shorts with the Corvettes in the parking lot.  You'd be surprised what people do now they've theoretically got nothing to do with their time!  Exercising is a GREAT way to pass the time in your elderly days and an even BETTER way to help you feel younger.  It would be accurate to say that 3 out of 10 people who come to me for exercise and nutritional coaching are over the age of 60 and have either retired or are still working because if they didn't they'd go nuts! <br />
<br />
I don't have hard and fast statistics but I bet the fitness success of those seniors is on par with any other age group I work with.  And what is fitness success you might ask?  Having been a personal trainer for 30 years I've determined that success in relationship to health and fitness is little more than consistency.  Whether that's as a client at the gym or working out at home as a member of my online training program.  One simple and easy, yet incredibly challenging word to accomplish – consistency. <br />
<br />
Aesop's ‘The Tortoise and the Hare' has so much significance when it comes to health and fitness.  Especially for the elderly who are making their first foray back into the gym in the last 20 or 30 years.  I hate to break it to you mister pot-bellied, knobblykneed, hair sprouting from strange places, Charles Atlas wannabe, but you're going to feel like someone performed open heart surgery on you without anesthetic if you try to bench press those weights today! <br />
<br />
I think the best way to describe how to get results, or conversely, how not to get results from joining a gym at your elderly age, would be to offer up a couple of real life examples.  Let's quickly go over the very simple procedure that will guarantee failure 99 per cent of the time.  It goes like this:<br />
1	Look in the mirror and realize you've got to exercise as you're body looks more elderly than you're actual age.<br />
2	You and elderly body join a gym<br />
3	Go to said gym every day for the first 7 – 10 days and start where you left off 10, 20 or 30 years ago…<br />
4	Quit gym – because nothing has changed…you hate exercise!<br />
<br />
Now, let's take a look at a couple of my “elderly” clients. I like to use the term elderly loosely as you are only as old as the body feels.  The most important thing you can do when jumping into a new exercise routine is to be brutally honest with yourself regarding 2 things.  First, how many days can you realistically devote to exercise, AND feel confident that you'll be able to stick to that schedule?  The second is to reflect back on exercise or working out back in the days when you did.  If it was a chore then, trust me when I say nothing has changed.  If you enjoyed it back then – you're weird!  <br />
<br />
So, elderly client number one (Whom We'll Call Madam A) and I met in March of 2008.  Her goals were, predictably ‘to lose weight and tone up through exercise'.  She informed me that in her not so senior days she used to be a runner…Hence she had never really exercised or worked out in a gym before.  Maddam A is a busy senior, like many in their elderly age… so we agreed that to start off with she would restrict her attendance to the gym to no more than 4 days a week – this was a realistic exercise attendance goal.<br />
<br />
We met that first Monday morning and I gave her the following routine.<br />
<br />
Exercises For the Elderly<br />
<br />
Stationary bike – 10 minute warm up<br />
Leg exercise<br />
Chest exercise<br />
Back exercise<br />
Shoulders exercise<br />
Triceps exercise<br />
Biceps exercise<br />
Stationary bike – 5 minute cool down<br />
<br />
Then I told her to take Tuesday off and to come back in on Wednesday and ride the exercise bike for 20 minutes and walk on the treadmill for 20 minutes.  Thursday was also a recovery day with Friday being exactly the same as Monday.  Her instinct was to do more exercises.  More exercise days, more weight, more repetitions, and more minutes.  To which I politely requested she refrain from.  Here's how I see it.  If you work out for an average of 4 days a week for a year then you're ahead of the game.  But we're talking about a whole year and you have reached elderly status.  Slow and steady wins the race.  We arranged to meet the following Monday where I planned on increasing her workload.  And, going over her food diary.  Yes, A food diary.  Any personal trainer will tell you that it's impossible to out train poor nutrition, especially for the elderly.<br />
<br />
At approximately the same time as I started training Madam A, I started training Mister X. Like many males in their elderly age Mister X plays golf to pass some of his time. Mister X wanted to exercise to gain weight and muscle for his golf game.  The exercise program I gave Mister X was almost identical to the exercise program for Madam A, food diary included!  Only difference was Mister X's exercises called for slightly heavier weights and fewer repetitions.     <br />
<br />
